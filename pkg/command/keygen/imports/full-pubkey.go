package imports

import (
	"errors"
	"fmt"

	"github.com/hiromaily/go-crypto-wallet/pkg/wallet/wallets"
)

func runFullPubKey(wallet wallets.Keygener, fileName string) error {
	fmt.Println("import full pubkey key generated by sign wallet")

	// validator
	if fileName == "" {
		return errors.New("file option [-file] is required")
	}

	// import generated private key to keygen wallet
	err := wallet.ImportFullPubKey(fileName)
	if err != nil {
		return fmt.Errorf("fail to call ImportFullPubKey() %w", err)
	}
	fmt.Println("Done!")

	return nil
}
